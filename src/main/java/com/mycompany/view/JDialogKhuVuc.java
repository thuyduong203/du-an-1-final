    public JDialogKhuVuc(java.awt.Frame parent, boolean modal) {
        super(parent, modal);
        initComponents();
        tblKhuVuc.setModel(dtmKhuVuc);
        String header[] = {"Mã khu vực", "Tên khu vực", "trạng thái"};
        dtmKhuVuc.setColumnIdentifiers(header);
        listKV = khuVucService.getAll();
        showData(listKV);
    }

    private void showData(List<KhuVuc> listKV) {
        dtmKhuVuc.setRowCount(0);
        for (KhuVuc khuVuc : listKV) {
            dtmKhuVuc.addRow(khuVuc.toDataRow());
        }
    }

    private KhuVuc newKhuVuc() {
        KhuVuc khuVuc = new KhuVuc();
        khuVuc.setMaKV(txtMaKhuVuc.getText());
        khuVuc.setTenKV(txtTenKhuVuc.getText());
        khuVuc.setTrangThai(Integer.valueOf(txtTrangThai.getText()));
        return khuVuc;
    }

